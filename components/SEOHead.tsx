import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOHeadProps {
  title?: string;
  description?: string;
  keywords?: string;
  canonical?: string;
  ogImage?: string;
  structuredData?: object;
}

export function SEOHead({
  title = 'PeakAI - LinkedIn Phone Number Finder with 91% Accuracy | ₹5/Contact',
  description = 'Find verified phone numbers from LinkedIn profiles with 91% accuracy. Only ₹5 for first 100 contacts vs ₹60 competitors. 100% credit refund guarantee.',
  keywords = 'LinkedIn phone finder, LinkedIn phone number, contact information, sales prospecting, chrome extension, phone numbers',
  canonical,
  ogImage = 'https://thepeakai.com/og-image.jpg',
  structuredData,
}: SEOHeadProps) {
  return (
    <Helmet>
      <title>{title}</title>
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      
      {canonical && <link rel="canonical" href={canonical} />}
      
      {/* Open Graph */}
      <meta property="og:title" content={title} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={ogImage} />
      {canonical && <meta property="og:url" content={canonical} />}
      
      {/* Twitter */}
      <meta name="twitter:title" content={title} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:image" content={ogImage} />
      
      {/* Structured Data */}
      {structuredData && (
        <script type="application/ld+json">
          {JSON.stringify(structuredData)}
        </script>
      )}
    </Helmet>
  );
}